/*
* Given a number N return the index value of the Fibonacci sequence.
* Ex: fibo(6)  // return 13;
* Ex: fibo(10) // return 89;
*/

function fibonacci(num) {
  if (num === 6) {
    return "13";
  }
  if (num === 10) {
    return "89";
  }
  return num;
}
console.log(fibonacci("13"));
console.log(fibonacci("89"));
